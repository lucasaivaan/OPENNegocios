package com.open.applic.open.interface_principal.nav_header.Sistema_pedidos.adaptadores;

public class adaptador_direccion {
    public adaptador_direccion() { }

    private String id;
    private String calle;
    private String numero;
    private String piso_depto;
    private String entre_calles;
    private String ciudad;
    private String localidad;


    public String getId() { return id; }
    public String getCalle() { return calle; }
    public String getNumero() { return numero; }
    public String getPiso_depto() { return piso_depto; }
    public String getEntre_calles() { return entre_calles; }
    public String getCiudad() { return ciudad; }
    public String getLocalidad() { return localidad; }


    public void setId(String id) { this.id = id; }
    public void setCalle(String calle) { this.calle = calle; }
    public void setNumero(String numero) { this.numero = numero; }
    public void setPiso_depto(String piso_depto) { this.piso_depto = piso_depto; }
    public void setEntre_calles(String entre_calles) { this.entre_calles = entre_calles; }
    public void setCiudad(String ciudad) { this.ciudad = ciudad; }
    public void setLocalidad(String localidad) { this.localidad = localidad; }

}
